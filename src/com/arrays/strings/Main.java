package com.arrays.strings;

public class Main {
    public static void main(String[] args) {
        //Check Unique Chars in a String
        System.out.println("Sai Krishna=>"+StringUniqueChars.checkUniqueChars("Sai Krishna"));
        System.out.println("Sai=>"+StringUniqueChars.checkUniqueChars("Sai"));
        System.out.println("1234567890=>"+StringUniqueChars.checkUniqueChars("1234567890"));
        System.out.println("12345678900=>"+StringUniqueChars.checkUniqueChars("12345678900"));
    }
}